  // Fetch new customer count (e.g., added within the last 24 hours)
  $newCustomerCountQuery = "SELECT COUNT(*) AS count FROM Customer WHERE created_at >= DATE_SUB(NOW(), INTERVAL 24 HOUR)";
  $newCustomerCountResult = $conn->query($newCustomerCountQuery);
  $newCustomerCount = 0;
  
  if ($newCustomerCountResult->num_rows > 0) {
    $row = $newCustomerCountResult->fetch_assoc();
    $newCustomerCount = $row['count'];
  }

  // Close the database connection
  $conn->close();
